  		  <td><div align="justify">A body at a finite temperature emits electromagnetic radiation. Higher the temperature more the radiation it emits.  The temperature of the sun is approximately 5700 K. Thus the sun emits a large amount of electromagnetic radiation which can be computed using the Stefan-Boltzmann law [3]. Most of sun's energy flows out of our solar system without colliding with anything. However a very small fraction of that energy does get intercepted by the Earth. If the earth were closer to the sun say at half the distance, the energy that the Earth would receive would be four times. This relationship is called the inverse square law of radiation. Just outside the Earth's atmosphere (extraterrestrial irradiance) about 1350 W/m<sub>2</sub> is received from the sun [1]. But at the surface of the Earth this is about 1000 W/m<sub>2</sub> since only approximately 75 % of the radiation penetrates through the Earth's atmosphere.</div></td>
